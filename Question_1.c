#include<stdio.h>
int main(){
    
    int arr[5] = {99,97,90,96,95};
    int sum = 0;
    
    for(int i=0; i<=5; i++){
        sum=sum+arr[i];
        
    }
    printf("sum of array in marks %d\n",sum);
    printf("average of arrays in marks %d\n",sum/2);

    
    return 0;

}


























